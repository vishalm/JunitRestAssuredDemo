public class SimpleCalculator {
    public static double divide(double a, double b) {
        return a / b;
    }
    public static double multiply(double a, double b) {
        return a * b;
    }
    public static int add(int c, int d) {
        return c + d;
    }
    public static int subtraction(int a, int b) {
        return a - b;
    }
}
